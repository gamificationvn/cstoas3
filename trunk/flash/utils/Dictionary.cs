namespace flash.utils {
	public class Dictionary {
		/// <summary>
		/// Creates a new Dictionary object.
		/// </summary>
		public Dictionary(bool weakKeys)
		{
		}

		/// <summary>
		/// Creates a new Dictionary object.
		/// </summary>
		public Dictionary()
		{
		}

		public object this[object i] {
			get {
				return null;
			}

			set {
				value = i;
			}
		}
	}
}
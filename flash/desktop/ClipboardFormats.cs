namespace flash.desktop {
	public enum ClipboardFormats {
		/// <summary>
		/// Image data (AIR only).
		/// </summary>
		BITMAP_FORMAT,

		/// <summary>
		/// An array of files (AIR only).
		/// </summary>
		FILE_LIST_FORMAT,

		/// <summary>
		/// HTML data.
		/// </summary>
		HTML_FORMAT,

		/// <summary>
		/// Rich Text Format data.
		/// </summary>
		RICH_TEXT_FORMAT,

		/// <summary>
		/// String data.
		/// </summary>
		TEXT_FORMAT,

		/// <summary>
		/// A URL string (AIR only).
		/// </summary>
		URL_FORMAT
	}
}
